    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        
        int hallId;
        int Charge;
        int MaxVolume;
        
        hallId = Integer.parseInt(hallID.getText());
        Charge = Integer.parseInt(charge.getText());
        MaxVolume = Integer.parseInt(maxVolume.getText());
        
        try {
            
            String sql = "INSERT INTO hall(Hall_ID,Charge,Max_Volume) VALUES('"+hallId+"','"+Charge+"','"+MaxVolume+"')";
            pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Hall added");
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
        
        tableload();
        
    }//GEN-LAST:event_addActionPerformed

