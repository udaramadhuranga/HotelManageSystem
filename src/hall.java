    public void tableload() {
    
        try {
            
            String sql = "SELECT * FROM hall";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            hallTable.setModel(DbUtils.resultSetToTableModel(rs));
            
            
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
      
    }
    
    public void tabledata() {
        
        int r = hallTable.getSelectedRow();
        
        String hallId = hallTable.getValueAt(r, 0).toString();
        String Charge = hallTable.getValueAt(r, 1).toString();
        String max = hallTable.getValueAt(r, 2).toString();
        
        hallID.setText(hallId);
        charge.setText(Charge);
        maxVolume.setText(max);
    }
    
    public void update() {
    
        String id = hallID.getText();
        String Charge = charge.getText();
        String max = maxVolume.getText();
        
        try {
            
            String sql = "UPDATE hall SET Charge='"+Charge+"',Max_Volume='"+max+"' WHERE Hall_ID = '"+id+"'";
            pst = conn.prepareStatement(sql);
            pst.execute();  
            JOptionPane.showMessageDialog(null, "Updated");
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
                
    }
    

