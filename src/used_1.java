    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        try {
           
            
            
            // TODO add your handling code here:
            int co = Integer.parseInt(code.getText());
           int qu = Integer.parseInt(quan.getText());
        //   String day = date.getText();
           String de = des.getText();
            
            pst =con.prepareStatement("insert into usedhistory(Icode,usedQuantity,date,description) values(?,?,?,?)");
            
            
            
           
           pst.setInt(1,co);
           pst.setInt(2, qu);
           pst.setString(3,((JTextField)date.getDateEditor().getUiComponent()).getText());
           pst.setString(4, de);
           
           pst.executeUpdate();
           
           JOptionPane.showMessageDialog(this, "recorde add to the used table successfully");
           name.setText("");
           code.setText("");
           quan.setText("");
         
  
           des.setText("");
           
           name.requestFocus();
           
           
           load();
            
        } catch (SQLException ex) {
            Logger.getLogger(used.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_addActionPerformed

