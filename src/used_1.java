    public void used(){
    
        String ing = name.getText();
        try {
            pst =con.prepareStatement("select * from foodingridient where name = ?" );
            pst.setString(1, ing);
            
            rs = pst.executeQuery();
            
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(this,"Ingridient not found");
                
                code.setToolTipText("");
                
            }
            
            
            else {
            
                String icode = rs.getString("ICode");
  
                code.setText(icode.trim());
                
               quan.requestFocus();
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(used.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
    public void load(){
    
        int a;
        try {
            pst= con.prepareStatement("select u.historyId,f.name,u.Icode,u.usedQuantity,u.date,u.description from usedhistory u,foodingridient f  where f.ICode = u.Icode");
            
             ResultSet rs = pst.executeQuery();
             
             ResultSetMetaData rd =rs.getMetaData();
             a=rd.getColumnCount();
             df =(DefaultTableModel)uta.getModel();
             df.setRowCount(0);
             
             while(rs.next())
                     {
                            Vector v2 = new Vector();
                            for(int i=1;i<a;i++){
                                
                                
                               v2.add(rs.getInt("u.historyId"));
                                v2.add(rs.getString("f.name"));
                                v2.add(rs.getInt("Icode"));
                                v2.add(rs.getInt("usedQuantity"));
                                v2.add(rs.getString("date"));
                                v2.add(rs.getString("description"));
                            }
                            
                            df.addRow(v2);
                     }
                     
                     
        
        } catch (SQLException ex) {
            Logger.getLogger(used.class.getName()).log(Level.SEVERE, null, ex);
        }
    
   
    }
            

   
    
