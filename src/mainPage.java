    public ArrayList<ExpensesBeans>retrieveData(){
        
        ArrayList<ExpensesBeans> al=null;
        al =new ArrayList<ExpensesBeans>();
        try {
            
            Connection conn=MySqlConnection();
            String qry= "select * from expenses ";
            Statement st =conn.createStatement();
            ResultSet rs= st.executeQuery(qry);
            ExpensesBeans expenses;
            
            while (rs.next()){
               expenses = new ExpensesBeans(rs.getInt(1),rs.getString(2),rs.getString(3),rs.getString(4),Float.parseFloat(rs.getString(5)));
               al.add(expenses);
               
                
            }
        } catch (Exception e) {
            
            System.err.println("Error in retrieveData method"+ e);
            
        }
       return al;
    }
    
    public void fillTabel(){
        
        ArrayList<ExpensesBeans> al = retrieveData();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.setRowCount(0);
        
        Object[] row=new Object[5];
       
        for(int i = 0; i<al.size(); i++){
            
            row[0]=al.get(i).getAccID();
            row[1]=al.get(i).getDescription();
            row[2]=al.get(i).getExpensesTypeNo();
            row[3]=al.get(i).getDate();
            row[4]=al.get(i).getAmount();
            model.addRow(row);
        }
        jTable1.setModel(model);
    }
    
