    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        // TODO add your handling code here:
        
        if (j_id.getText()!=null || j_des.getText()!= null || j_etn.getText()!= null || j_date!= null || j_amount.getText()!= null) {
            
            try {
                Connection conn= MySqlConnection();
                
                PreparedStatement ps = conn.prepareStatement("INSERT INTO  expenses (AccID, Description,ExpensesTypeNo ,Date, Amount) VALUES " + "(?,?,?,?,?)");
                ps.setInt(1, Integer.parseInt(j_id.getText()));
                ps.setString(2, j_des.getText());
                ps.setString(3, j_etn.getText());
                SimpleDateFormat sdf=new SimpleDateFormat("dd-MM-yyyy");
                String date1 =sdf.format(j_date.getDate());
                ps.setString(4, date1);
                ps.setFloat(5, Float.parseFloat(j_amount.getText()));
                
                int res =ps.executeUpdate();
                if(res>=1){
                    
                    JOptionPane.showConfirmDialog(null, res+"Number of One Expensne" + "Insert in to database....");
                }else{
                    
                   JOptionPane.showConfirmDialog(null, "Insert fail...."); 
                }
                
               fillTabel(); 
            } catch (Exception e) {
                
                 
            }
        }
    }//GEN-LAST:event_btn_addActionPerformed

